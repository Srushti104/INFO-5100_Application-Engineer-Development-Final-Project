    private void reportAnIncidentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportAnIncidentButtonActionPerformed
        // TODO add your handling code here:
        for (Network network : business.getNetworkDirectory().getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount1 : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    if (userAccount == userAccount1) {
                        ReportAnIncidentJPanel reportAnIncidentJPanel = new ReportAnIncidentJPanel(userProcessContainer, network, userAccount);
                        userProcessContainer.add("Report", reportAnIncidentJPanel);

                        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                        cardLayout.next(userProcessContainer);
                    }
                }
            }
        }


    }//GEN-LAST:event_reportAnIncidentButtonActionPerformed

