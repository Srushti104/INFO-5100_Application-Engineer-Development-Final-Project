    private void addDoctorBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDoctorBtnActionPerformed
        // TODO add your handling code here:
        for (Network network : business.getNetworkDirectory().getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrgList()) {
                    for (UserAccount userAccount1 : organization.getUserAccountDirectory().getUserAccountList()) {
                        if (userAccount == userAccount1) {

                            AddDoctorJPanel addDoctorJPanel = new AddDoctorJPanel(userProcessContainer, network, userAccount);
                            userProcessContainer.add("AddDoctor", addDoctorJPanel);

                            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                            cardLayout.next(userProcessContainer);

                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_addDoctorBtnActionPerformed

    private void viewReportedIncidentsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewReportedIncidentsButtonActionPerformed
        // TODO add your handling code here:

        for (Network network : business.getNetworkDirectory().getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrgList()) {
                    for (UserAccount userAccount1 : organization.getUserAccountDirectory().getUserAccountList()) {
                        if (userAccount == userAccount1) {

                            ReportedIncidentsJPanel reportedIncidentsJPanel = new ReportedIncidentsJPanel(userProcessContainer, network, userAccount);
                            userProcessContainer.add("ReportInc", reportedIncidentsJPanel);

                            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                            cardLayout.next(userProcessContainer);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_viewReportedIncidentsButtonActionPerformed

