    private void sendRequestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendRequestButtonActionPerformed
        // TODO add your handling code here:
        if(manuNameField.getText().isEmpty() || drugaManuTextArea.getText().isEmpty() || messageField.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter the values for all fields");
        }
        
        else
        {
        
        LicenseManagerWorkRequest licenseManagerWorkRequest=new LicenseManagerWorkRequest();
        licenseManagerWorkRequest.setSender(userAccount);
        licenseManagerWorkRequest.setRequestDate(new Date());
        licenseManagerWorkRequest.setMessage(manuNameField.getText());
        licenseManagerWorkRequest.setStatus("License Request Sent");
        licenseManagerWorkRequest.setManuName(manuNameField.getText());
        licenseManagerWorkRequest.setDrugManu(drugaManuTextArea.getText());
        licenseManagerWorkRequest.setResult(messageField.getText());
       
        
        DistributorEnterprise e=(DistributorEnterprise)network.getEnterpriseDirectory().getMyEnterprise(userAccount);
       
        StateRegulatoryEnterprise e1 = null;
        for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList())
        {
            if(enterprise.getClass().equals(StateRegulatoryEnterprise.class))
                
            {
                e1=(StateRegulatoryEnterprise)enterprise;
            }
        }
        
        
        LicensingOrganization licensingOrganization=e1.getLicensingOrganization();
        licensingOrganization.getWorkQueue().getWorkRequestList().add(licenseManagerWorkRequest);
        
       // userAccount.getWorkQueue().getWorkRequestList().add(salesRequest);
        e.getManagementOrganization().getSentWorkQueue().getWorkRequestList().add(licenseManagerWorkRequest);
        
        
        JOptionPane.showMessageDialog(null, "Work Request Sent");
        }
    }//GEN-LAST:event_sendRequestButtonActionPerformed

