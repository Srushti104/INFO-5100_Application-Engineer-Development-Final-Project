    private void viewDrugIncidentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDrugIncidentsActionPerformed
        // TODO add your handling code here:
        for (Network network : business.getNetworkDirectory().getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrgList()) {
                    for (UserAccount userAccount1 : organization.getUserAccountDirectory().getUserAccountList()) {

                        if (userAccount == userAccount1) {

                            ViewDrugIncidentsJPanel viewDrugIncidentsJPanel = new ViewDrugIncidentsJPanel(userProcessContainer, network, userAccount);
                            userProcessContainer.add("viewDrugIncidents", viewDrugIncidentsJPanel);

                            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                            cardLayout.next(userProcessContainer);
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_viewDrugIncidentsActionPerformed

